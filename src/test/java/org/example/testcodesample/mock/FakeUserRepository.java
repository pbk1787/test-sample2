package org.example.testcodesample.mock;

import java.util.ArrayList;
import java.util.List;
import java.util.Optional;
import java.util.concurrent.atomic.AtomicLong;
import org.example.testcodesample.user.domain.User;
import org.example.testcodesample.user.domain.UserStatus;
import org.example.testcodesample.user.service.port.UserRepository;

public class FakeUserRepository implements UserRepository {

    private final AtomicLong autoGeneratedId = new AtomicLong(0);
    private final List<User> data = new ArrayList<>();

    @Override
    public Optional<User> findById(long id) {
        return data.stream()
            .filter(user -> user.getId().equals(id))
            .findAny();
    }

    @Override
    public Optional<User> findByIdAndStatus(long id, UserStatus userStatus) {
        return data.stream()
            .filter(user -> user.getId().equals(id) && user.getStatus().equals(userStatus))
            .findAny();
    }

    @Override
    public Optional<User> findByEmailAndStatus(String email, UserStatus userStatus) {
        return data.stream()
            .filter(user -> user.getEmail().equals(email) && user.getStatus().equals(userStatus))
            .findAny();
    }

    @Override
    public User save(User user) {
        if (user.getId() == null || user.getId() == 0) {
            User newUser = User.builder()
                .id(autoGeneratedId.incrementAndGet())
                .email(user.getEmail())
                .nickname(user.getNickname())
                .address(user.getAddress())
                .certificationCode(user.getCertificationCode())
                .status(user.getStatus())
                .lastLoginAt(user.getLastLoginAt())
                .build();
            data.add(newUser);
            return newUser;
        } else {
            data.removeIf(it -> it.getId().equals(user.getId()));
            data.add(user);
            return user;
        }
    }
}
